	<h1>WHAT IS CONTEMPORARY CIRCUS?</h1>
<p>
Contemporary circus is a modern genre in the performing arts that mixes classic circus disciplines with elements from dance, theater and performance. The genre originated in the mid-1970s in France.
</p>

<p>
In a completely traditional circus, you can typically experience a number of different artist or animal numbers, which present something extraordinary and unusual, preferably presented in a round manege in a circus tent.
</p>
<p>
In modern neo-circus, one uses some of the same circus disciplines, but one puts them together for a different kind of performance. A contemporary circus performance is most often a unified narrative with an unified expression. It can e.g. be because the performance has a concrete action and tells a collective story, or it may be because everything in the performance is about the same theme.
</p>
<p>
In contemporary circuses, there are rarely trained animals. It is the circus artist and body balancer who is at the center. Contemporary circus artists are often educated in circus schools, where they have taught circus disciplines, acrobatics, dance and theater.
</p>
<p>
Contemporary circus performances are performed everywhere, both on ordinary theater stages, outdoors in the urban space and in small and large circus tents.
</p>
<p>
In Denmark, active contemporary circus groups have existed for over 20 years. Danish contemporary circus artists can also often be experienced both on established theater stages and at theater festivals, street theater festivals, alternative venues, schools and cultural centers, etc., both at home and abroad.
</p>

